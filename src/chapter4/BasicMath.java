package chapter4;

//Продемонстрировать основные математические операции
public class BasicMath {
    public static void main(String[] args) {
        //арифимитически операции над целочисленными значениями
        System.out.println("Целочисленная арифметика");
        int a = 1 + 1;
        int b = a * 3;
        int c = b / 4;
        int d = c - a;
        int e = -d;
        System.out.println();



        //Арифмитические операции над значениями типа double
        System.out.println("Арифметика с плавающей точкой");
        double da = 1 + 1;
        double db = da * 3;
        double dc = db / 4;
        double dd = dc - da;
        double de = -dd;



    }
}
